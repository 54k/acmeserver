package com.acme.gameserver.template;

import java.util.List;

public class ChestAreaTemplate {

    private int x;
    private int y;
    private int w;
    private int h;
    private List<Integer> i;
    private int tx;
    private int ty;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getW() {
        return w;
    }

    public int getH() {
        return h;
    }

    public List<Integer> getI() {
        return i;
    }

    public int getTx() {
        return tx;
    }

    public int getTy() {
        return ty;
    }
}