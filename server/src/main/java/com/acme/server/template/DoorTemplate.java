package com.acme.server.template;

public class DoorTemplate {

    private int x;
    private int y;
    private int p;
    private int tcx;
    private int tcy;
    private String to;
    private int tx;
    private int ty;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getP() {
        return p;
    }

    public int getTcx() {
        return tcx;
    }

    public int getTcy() {
        return tcy;
    }

    public String getTo() {
        return to;
    }

    public int getTx() {
        return tx;
    }

    public int getTy() {
        return ty;
    }
}

